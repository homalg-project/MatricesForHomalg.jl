"""
    HomalgMatrix(entries, r, c, R::Singular.PolyRing)

Construct a Singular matrix of size `r × c` over the polynomial ring R,
with entries given by the (possibly nested) array `entries`.

```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, (x, y) = Singular.polynomial_ring(Singular.QQ, ["x", "y"]);

julia> HomalgMatrix([[x, y], [x^2, y^2]], 2, 2, R)
[x, y
x^2, y^2]
```
"""
function MatricesForHomalg.HomalgMatrix(entries, nr_rows, nr_cols, R::Singular.PolyRing)
    m = Singular.zero_matrix(R, nr_rows, nr_cols)
    flat = vcat(entries...)
    for i in 1:nr_rows, j in 1:nr_cols
        m[i, j] = R(flat[(i - 1) * nr_cols + j])
    end
    return m
end

