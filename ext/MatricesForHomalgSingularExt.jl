## DecideZeroRows / DecideZeroColumns

"""
    DecideZeroColumns(B::Singular.smatrix, A::Singular.smatrix)

Reduce each column of B modulo the column module of A.
Returns a matrix whose columns are the normal forms of the columns of B with respect to the column Gröbner basis of A.

```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, (x, y) = Singular.polynomial_ring(Singular.QQ, ["x", "y"]);

julia> B = HomalgMatrix([[x^2], [y^2]], 2, 1, R);

julia> N = HomalgMatrix([[x], [y]], 2, 1, R);

julia> DecideZeroColumns(B, N)
[0
-x*y + y^2]
```
"""
function MatricesForHomalg.DecideZeroColumns(B::Singular.smatrix, A::Singular.smatrix)
    M_A = Singular.Module(A)
    G = Singular.std(M_A)
    M_B = Singular.Module(B)
    R = Singular.reduce(M_B, G)
    return Singular.Matrix(R)
end

"""
    DecideZeroRows(B::Singular.smatrix, A::Singular.smatrix)

