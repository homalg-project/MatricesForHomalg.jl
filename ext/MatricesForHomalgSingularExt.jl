    end
end

## ReducedSyzygiesOfRows / ReducedSyzygiesOfColumns

function MatricesForHomalg.ReducedSyzygiesOfColumns(A::Singular.smatrix)
    M = Singular.Module(A)
    S = Singular.syz(M)
    if Singular.iszero(S)
        R = Singular.base_ring(A)
        return Singular.zero_matrix(R, Singular.ncols(A), 0)
    end
    G = Singular.std(S; complete_reduction=true)
    return Singular.Matrix(G)
end

function MatricesForHomalg.ReducedSyzygiesOfRows(A::Singular.smatrix)
    return Singular.transpose(MatricesForHomalg.ReducedSyzygiesOfColumns(Singular.transpose(A)))
end

