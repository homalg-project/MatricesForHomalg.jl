## Parse a polynomial string into an element of the ring R

function _subst_vars(expr, vars::Dict{Symbol})
    expr isa Symbol && haskey(vars, expr) ? vars[expr] :
    expr isa Expr ? Expr(expr.head, (_subst_vars(a, vars) for a in expr.args)...) :
    expr
end

function (R::Singular.PolyRing)(s::String)
    vars = Dict(sym => g for (sym, g) in zip(R.S, Singular.gens(R)))
    return Core.eval(@__MODULE__, _subst_vars(Meta.parse(s), vars))
end

