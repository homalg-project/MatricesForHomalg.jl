## RandomMatrix for Singular rings
function MatricesForHomalg.RandomMatrix(r, c, R::Singular.PolyRing)
    m = Singular.zero_matrix(R, r, c)
    for i in 1:r, j in 1:c
        m[i, j] = R(rand(-5:5)) * prod([rand([zero(R); one(R); Singular.gens(R)...]) for i in 1:rand([1, 1, 1, 2, 2, 3])])
    end
    return m
end

function Base.:-(mat::Singular.smatrix)
    R = Singular.base_ring(mat)
    result = Singular.zero_matrix(R, Singular.nrows(mat), Singular.ncols(mat))
    for i in 1:Singular.nrows(mat), j in 1:Singular.ncols(mat)
        result[i, j] = -mat[i, j]
    end
    return result
end

