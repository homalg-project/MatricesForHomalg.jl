"""
    RingName(R::Singular.PolyRing)

Return a descriptive name string for the polynomial ring R,
including quotient relations when R is a quotient ring.

```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, (x, y) = Singular.polynomial_ring(Singular.QQ, ["x", "y"]);

julia> RingName(R)
"Q[x,y]"

julia> S = R / [x^2 + y^2 - 1];

julia> RingName(S)
"Q[x,y] / ( x^2 + y^2 - 1 )"
```
"""
function MatricesForHomalg.RingName(R::Singular.PolyRing)
    base = RingName(Singular.base_ring(R))
    vars = join(map(s -> string(s), R.S), ",")
    name = base * "[" * vars * "]"
    if Singular.is_quotient_ring(R)
        I = Singular.quotient_ideal(R)
        rels = join([string(Singular.gens(I)[k]) for k in 1:Singular.ngens(I)], ", ")
        name *= " / ( " * rels * " )"
    end
    return name
end

