## IsZero / IsOne / IsEmptyMatrix / IsSymmetricMatrix for smatrix

function MatricesForHomalg.IsZero(mat::Singular.smatrix)
    return Singular.iszero(mat)
end

function MatricesForHomalg.IsOne(mat::Singular.smatrix)
    R = Singular.base_ring(mat)
    nr = Singular.nrows(mat)
    nc = Singular.ncols(mat)
    nr == nc || return false
    for i in 1:nr, j in 1:nc
        if i == j
            mat[i, j] == R(1) || return false
        else
            Singular.iszero(mat[i, j]) || return false
        end
    end
    return true
end

function MatricesForHomalg.IsEmptyMatrix(mat::Singular.smatrix)
    return Singular.nrows(mat) == 0 || Singular.ncols(mat) == 0
end

function MatricesForHomalg.IsSymmetricMatrix(mat::Singular.smatrix)
    nr = Singular.nrows(mat)
    nc = Singular.ncols(mat)
    nr == nc || return false
    for i in 1:nr, j in (i+1):nc
        mat[i, j] == mat[j, i] || return false
    end
    return true
end

## CertainRows / CertainColumns for smatrix

function MatricesForHomalg.CertainRows(mat::Singular.smatrix, list)
    R = Singular.base_ring(mat)
    nc = Singular.ncols(mat)
    nr_new = length(list)
    if nr_new == 0
        return Singular.zero_matrix(R, 0, nc)
    end
    result = Singular.zero_matrix(R, nr_new, nc)
    for (i, row) in enumerate(list)
        for j in 1:nc
            result[i, j] = mat[Int(row), j]
        end
    end
    return result
end

function MatricesForHomalg.CertainColumns(mat::Singular.smatrix, list)
    R = Singular.base_ring(mat)
    nr = Singular.nrows(mat)
    nc_new = length(list)
    if nc_new == 0
        return Singular.zero_matrix(R, nr, 0)
    end
    result = Singular.zero_matrix(R, nr, nc_new)
    for (j, col) in enumerate(list)
        for i in 1:nr
            result[i, j] = mat[i, Int(col)]
        end
    end
    return result
end

## UnionOfRows / UnionOfColumns for Singular rings

function MatricesForHomalg.UnionOfRows(R::Singular.PolyRing, nr_cols, list)
    if length(list) == 0
        return Singular.zero_matrix(R, 0, nr_cols)
    end
    total_rows = sum(Singular.nrows(m) for m in list)
    result = Singular.zero_matrix(R, total_rows, nr_cols)
    row_offset = 0
    for m in list
        nr = Singular.nrows(m)
        for i in 1:nr, j in 1:nr_cols
            result[row_offset + i, j] = m[i, j]
        end
        row_offset += nr
    end
    return result
end

function MatricesForHomalg.UnionOfColumns(R::Singular.PolyRing, nr_rows, list)
    if length(list) == 0
        return Singular.zero_matrix(R, nr_rows, 0)
    end
    total_cols = sum(Singular.ncols(m) for m in list)
    result = Singular.zero_matrix(R, nr_rows, total_cols)
    col_offset = 0
    for m in list
        nc = Singular.ncols(m)
        for i in 1:nr_rows, j in 1:nc
            result[i, col_offset + j] = m[i, j]
        end
        col_offset += nc
    end
    return result
end

## KroneckerMat for smatrix

function MatricesForHomalg.KroneckerMat(mat1::Singular.smatrix, mat2::Singular.smatrix)
    R = Singular.base_ring(mat1)
    r1, c1 = Singular.nrows(mat1), Singular.ncols(mat1)
    r2, c2 = Singular.nrows(mat2), Singular.ncols(mat2)
    result = Singular.zero_matrix(R, r1 * r2, c1 * c2)
    for i1 in 1:r1, j1 in 1:c1
        a = mat1[i1, j1]
        for i2 in 1:r2, j2 in 1:c2
            result[(i1 - 1) * r2 + i2, (j1 - 1) * c2 + j2] = a * mat2[i2, j2]
        end
    end
    return result
end

## HomalgDiagonalMatrix for Singular rings

function MatricesForHomalg.HomalgDiagonalMatrix(diagonal_entries, R::Singular.PolyRing)
    n = length(diagonal_entries)
    result = Singular.zero_matrix(R, n, n)
    for i in 1:n
        result[i, i] = R(diagonal_entries[i])
    end
    return result
end

"""
    SyzygiesOfRows(A::Singular.smatrix)

Compute the matrix of row syzygies of A using Singular.
Returns a matrix whose rows span the left kernel of A,
i.e. all rows X satisfying X * A = 0.

# Examples
```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, (x, y) = Singular.polynomial_ring(Singular.QQ, ["x", "y"])
(Singular polynomial ring (QQ),(x,y),(dp(2),C), spoly{Singular.n_Q}[x, y])

julia> A = Singular.Matrix(R, [x y; x^2 y^2; x*y x*y])
x*y
x^2*y^2
x*y*x*y

julia> S = SyzygiesOfRows(A)
x*(-y+x)*1
(-1)*(y+x)*x

julia> iszero(S * A)
true
```
"""
function MatricesForHomalg.SyzygiesOfRows(A::Singular.smatrix)
    R = Singular.base_ring(A)
    # Columns of transpose(A) are the rows of A.
    # Module from those columns, syz finds relations among them,
    # i.e. vectors (a1,...,am) with a1*row1 + ... + am*rowm = 0.
    M = Singular.Module(Singular.transpose(A))
    S = Singular.syz(M)
    # Matrix(S) has syzygy vectors as columns; transpose to get rows.
    if Singular.iszero(S)
        # syz returns zero module if there are no syzygies, but we want a zero matrix.
        return Singular.zero_matrix(R, 0, Singular.nrows(A))
    else
        return Singular.transpose(Singular.Matrix(S))
    end
end

"""
    SyzygiesOfColumns(A::Singular.smatrix)

Compute the matrix of column syzygies of A using Singular.
Returns a matrix whose columns span the right kernel of A,
i.e. all columns X satisfying A * X = 0.

# Examples
```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, (x, y) = Singular.polynomial_ring(Singular.QQ, ["x", "y"])
(Singular polynomial ring (QQ),(x,y),(dp(2),C), spoly{Singular.n_Q}[x, y])

julia> A = Singular.Matrix(R, [x x^2 x*y; y y^2 x*y])
x, x^2, x*y
y, y^2, x*y

julia> S = SyzygiesOfColumns(A)
...

julia> iszero(A * S)
true
```
"""
function MatricesForHomalg.SyzygiesOfColumns(A::Singular.smatrix)
    # Columns of A are generators of a submodule of R^m.
    # syz finds vectors (a1,...,an) with a1*col1 + ... + an*coln = 0,
    # which is exactly A * [a1,...,an]^T = 0.
    M = Singular.Module(A)
    S = Singular.syz(M)
    if Singular.iszero(S)
        # syz returns zero module if there are no syzygies, but we want a zero matrix.
        R = Singular.base_ring(A)
        return Singular.zero_matrix(R, Singular.ncols(A), 0)
    else
        return Singular.Matrix(S)
    end
end

"""
    SyzygiesOfRows(A::Singular.smatrix, N::Singular.smatrix)

Compute the matrix of relative row syzygies of A modulo N using Singular.
Returns a matrix whose rows K satisfy K * A + L * N = 0 for some L.

# Examples
```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, (x, y) = Singular.polynomial_ring(Singular.QQ, ["x", "y"])
(Singular polynomial ring (QQ),(x,y),(dp(2),C), spoly{Singular.n_Q}[x, y])

julia> v1 = Singular.vector(R, x, y)
x*gen(1)+y*gen(2)

julia> v2 = Singular.vector(R, y, x)
y*gen(1)+x*gen(2)

julia> A = Singular.Matrix(Singular.Module(R, v1, v2))
x*y
y*x

julia> N = Singular.Matrix(Singular.Module(R, v1))
x
y

julia> K = SyzygiesOfRows(A, N)
...

julia> # verify K * A is in the row span of N
```
"""
function MatricesForHomalg.SyzygiesOfRows(A::Singular.smatrix, N::Singular.smatrix)
    At = Singular.transpose(A)
    Nt = Singular.transpose(N)
    St = MatricesForHomalg.SyzygiesOfColumns(At, Nt)
    return Singular.transpose(St)
end

"""
    SyzygiesOfColumns(A::Singular.smatrix, N::Singular.smatrix)

Compute the matrix of relative column syzygies of A modulo N using Singular.
Returns a matrix whose columns K satisfy A * K + N * L = 0 for some L.

# Examples
```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, (x, y) = Singular.polynomial_ring(Singular.QQ, ["x", "y"])
(Singular polynomial ring (QQ),(x,y),(dp(2),C), spoly{Singular.n_Q}[x, y])

julia> v1 = Singular.vector(R, x, y)
x*gen(1)+y*gen(2)

julia> v2 = Singular.vector(R, y, x)
y*gen(1)+x*gen(2)

julia> A = Singular.Matrix(Singular.Module(R, v1, v2))
x*y
y*x

julia> N = Singular.Matrix(Singular.Module(R, v1))
x
y

julia> K = SyzygiesOfColumns(A, N)
...

julia> # verify A * K is in the column span of N
```
"""
function MatricesForHomalg.SyzygiesOfColumns(A::Singular.smatrix, N::Singular.smatrix)
    # modulo(M_A, M_N) computes the kernel of R^n -> M_A / (M_A ∩ M_N),
    # i.e. vectors (a1,...,an) such that a1*col1(A) + ... + an*coln(A) ∈ Im(N).
    M_A = Singular.Module(A)
    M_N = Singular.Module(N)
    S = Singular.modulo(M_A, M_N)
    if Singular.iszero(S)
        R = Singular.base_ring(A)
        return Singular.zero_matrix(R, Singular.ncols(A), 0)
    else
        return Singular.Matrix(S)
    end
end

