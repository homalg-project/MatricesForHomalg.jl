## RandomMatrix for Singular rings
"""
    RandomMatrix(r, c, R::Singular.PolyRing)

Construct a random `(r × c)` matrix over the Singular polynomial ring R,
with entries that are random scalar multiples of monomials in the generators of R.

```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, _ = Singular.polynomial_ring(Singular.QQ, ["x", "y"]);

julia> m = RandomMatrix(2, 3, R);

julia> NumberRows(m), NumberColumns(m)
(2, 3)
```
"""
function MatricesForHomalg.RandomMatrix(r, c, R::Singular.PolyRing)
    m = Singular.zero_matrix(R, r, c)
    for i in 1:r, j in 1:c
        m[i, j] = R(rand(-5:5)) * prod([rand([zero(R); one(R); Singular.gens(R)...]) for i in 1:rand([1, 1, 1, 2, 2, 3])])
    end
    return m
end

"""
    -(mat::Singular.smatrix)

Return the negation of the Singular matrix `mat`.

```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, (x, y) = Singular.polynomial_ring(Singular.QQ, ["x", "y"]);

julia> A = HomalgMatrix([[x, y], [x^2, y^2]], 2, 2, R);

julia> -A
[-x, -y
-x^2, -y^2]
```
"""
function Base.:-(mat::Singular.smatrix)
    R = Singular.base_ring(mat)
    result = Singular.zero_matrix(R, Singular.nrows(mat), Singular.ncols(mat))
    for i in 1:Singular.nrows(mat), j in 1:Singular.ncols(mat)
        result[i, j] = -mat[i, j]
    end
    return result
end

