"""
    IsOne(mat::Singular.smatrix)

Return `true` if `mat` is an identity matrix, otherwise `false`.

```jldoctest
julia> using Singular, MatricesForHomalg

julia> R, _ = Singular.polynomial_ring(Singular.QQ, ["x"]);

julia> IsOne(HomalgIdentityMatrix(2, R))
true
```
"""
function MatricesForHomalg.IsOne(mat::Singular.smatrix)
    R = Singular.base_ring(mat)
    nr = Singular.nrows(mat)
    nc = Singular.ncols(mat)
    nr == nc || return false
    for i in 1:nr, j in 1:nc
        if i == j
            mat[i, j] == R(1) || return false
        else
            Singular.iszero(mat[i, j]) || return false
        end
    end
    return true
end

